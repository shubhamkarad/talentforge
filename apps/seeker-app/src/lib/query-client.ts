import { QueryClient } from '@tanstack/react-query';

// Shared QueryClient singleton. Same defaults as the recruiter console so
// cached state feels identical across both apps.
export const queryClient = new QueryClient({
  defaultOptions: {
    queries: {
      staleTime: 5 * 60 * 1000,
      gcTime: 10 * 60 * 1000,
      retry: 1,
      refetchOnWindowFocus: false,
    },
    mutations: {
      retry: 0,
    },
  },
});
